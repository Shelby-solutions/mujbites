class CartItem {
  final String id;
  final String name;
  final double price;
  final String size;
  final String restaurantId;
  final String restaurantName;

  CartItem({
    required this.id,
    required this.name,
    required this.price,
    required this.size,
    required this.restaurantId,
    required this.restaurantName,
  });
} 